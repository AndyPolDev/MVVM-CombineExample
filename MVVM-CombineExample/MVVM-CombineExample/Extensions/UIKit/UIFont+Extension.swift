import UIKit

extension UIFont {
	
	static func avenirNextRegular(ofSize size: CGFloat) -> UIFont? {
		return UIFont.init(name: "AvenirNext-Regular", size: size)
	}

	static func avenirNextBold(ofSize size: CGFloat) -> UIFont? {
		return UIFont.init(name: "AvenirNext-Bold", size: size)
	}
	
	static func avenirNextDemiBold(ofSize size: CGFloat) -> UIFont? {
		return UIFont.init(name: "AvenirNext-DemiBold", size: size)
	}
}
